type TranslationKey = keyof (typeof translations)["zh"];

export function t(
  key: TranslationKey,
  locale: Locale = "zh",
  params?: Record<string, string | number>
): string {
  const dict = translations[locale] || translations.zh;
  let text = (dict as Record<string, string>)[key] || key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      text = text.replace(`{${k}}`, String(v));
    }
  }
  return text;
}

export function getStageLabel(
  stage: string,
  locale: Locale = "zh"
): string {
  return t(`stage.${stage}` as TranslationKey, locale);
}

const PRIORITY_KEYS = ["", "priority.lowest", "priority.low", "priority.medium", "priority.high", "priority.highest"] as const;

export function getPriorityLabel(
  priority: number,
  locale: Locale = "zh"
): string {
  return t(PRIORITY_KEYS[priority] as TranslationKey, locale);
}
